public class dec_function {

    public dec_function(){

    }


    //两个double类型的数字进行减法运算并返回double类型结果
    public Double DecDouble(double num1,double num2){
        return num1 - num2;
    }

    //三个double类型的数字进行减法运算并返回double类型结果
    public Double DecDouble(double num1,double num2,double num3){
        return num1 - num2 - num3;
    }

}
